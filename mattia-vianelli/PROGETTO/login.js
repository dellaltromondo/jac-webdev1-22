// da Login a Signin
function mostraRegistrazione() {
    document.getElementById("paginaLogin").style.display = "none"
    document.getElementById("paginaSignin").style.display = "block"
}
// da Signin a Login
function mostraLogin() {
    document.getElementById("paginaLogin").style.display = "block"
    document.getElementById("paginaSignin").style.display = "none"
}


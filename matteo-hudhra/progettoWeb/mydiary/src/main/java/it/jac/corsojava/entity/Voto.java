package it.jac.corsojava.entity;

public class Voto {

	private int idVoto;
	private double valoreVoto;
	private String descrizioneVoto;
	private int codMateria;
	
	public int getIdVoto() {
		return idVoto;
	}
	public void setIdVoto(int idVoto) {
		this.idVoto = idVoto;
	}
	public double getValoreVoto() {
		return valoreVoto;
	}
	public void setValoreVoto(double valoreVoto) {
		this.valoreVoto = valoreVoto;
	}
	public String getDescrizioneVoto() {
		return descrizioneVoto;
	}
	public void setDescrizioneVoto(String descrizioneVoto) {
		this.descrizioneVoto = descrizioneVoto;
	}
	public int getCodMateria() {
		return codMateria;
	}
	public void setCodMateria(int codMateria) {
		this.codMateria = codMateria;
	}
	
	
}

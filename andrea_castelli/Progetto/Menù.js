    let Nome="";
    let Pizza=new Array();
    let Bevanda=new Array();
    let Indirizzo="";
    let nCivico=0;
    let nTavolo=0;
    let Prezzo=0;

    function Ordine()
    {
        Nome=document.getElementById("nome").value;
        Pizza.push(document.getElementById("pizza").value);
        Bevanda.push(document.getElementById("bevande").value);

        for(let i=0; i<Menu.length; i++)
        {
            if(document.getElementById("pizza").value==Menu[i].Nome)
            {
                Prezzo+=Menu[i].Prezzo;
            }
        }
        for(let i=0; i<Bevande.length; i++)
        {
            if(document.getElementById("bevande").value==Bevande[i].Nome)
            {
                Prezzo+=Bevande[i].Prezzo;
            }
        }

        if(document.contains(document.getElementById("indirizzo"))&&document.contains(document.getElementById("nCivico")))
        {
            Indirizzo=document.getElementById("indirizzo").value;
            nCivico=document.getElementById("nCivico").value;
        }
    
        else
        {
            nTavolo=document.getElementById("nTavolo").value;
        }
    }

    function SalvaOrdine()
    {
        const col2=document.getElementById("colonna2");

        const sec=document.createElement("section");
        sec.setAttribute("id", "sectionOrdine");
        col2.appendChild(sec);

        if(document.contains(document.getElementById("indirizzo"))&&document.contains(document.getElementById("nCivico")))
        {
            const listaPizze=Pizza.toString();
            const listaBevande=Bevanda.toString();
            sec.innerText="Nome ordine: "+Nome+"\nIndirizzo: "+Indirizzo+"\nNumero civico: "+nCivico+"\nPizze: "+listaPizze+"\nBevande:"+listaBevande+"\nPrezzo: "+Prezzo+" €";
        }

        else
        {
            const listaPizze=Pizza.toString();
            const listaBevande=Bevanda.toString();
            sec.innerText="Nome ordine: "+Nome+"\nNumero tavolo: "+nTavolo+"\nPizze: "+listaPizze+"\nBevande: "+listaBevande+"\nPrezzo: "+Prezzo+" €";
        }

        Pizza=[];
        Bevanda=[];
        Prezzo=0;
    }
